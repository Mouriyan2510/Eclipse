package com.onesoft;

public class Float {
	public static void main(String []args) {
		float a=10f;
		float b=22f;
		System.out.println("Total: "+(a+b));
		System.out.println("Total: "+(a-b));
		System.out.println("Mul: "+(a*b));
		System.out.println("Div: "+(a/b));
		System.out.println("Mod: "+(a%b));	
		
	}

}

