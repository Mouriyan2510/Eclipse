package com.one.compositionOops;

public class Battery {
	private int capacity;
	private int price;
	
	public void setCapacity(int capacity) {
		this.capacity=capacity;
	}
	public int getCapacity() {
		return capacity;
	}
	
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}

}
