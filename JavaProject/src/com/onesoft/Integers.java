package com.onesoft;

public class Integers {
	public static void main(String []args) {
		int a=10;
		int b=20;
		int add=a+b;
		int sub=a-b;
		int mul=a*b;
		int div=a/b;
		int mod=a%b;
		System.out.println("Total: "+add);
		System.out.println("Total: "+sub);
		System.out.println("Total: "+mul);
		System.out.println("Total: "+div);
		System.out.println("Total: "+mod);		
	}

}
